import sys
import logging
from typing import List

# ======================================================================================================================
# Third party modules
# ======================================================================================================================
from pydantic import BaseModel

# ======================================================================================================================
# Logging configuration
# ======================================================================================================================
LOGGER = logging.getLogger(__name__)
LOGGER.setLevel(logging.DEBUG)

# ======================================================================================================================
# Model definitions
# ======================================================================================================================


class BaseItem(BaseModel):
    name: str


class CheapItem(BaseItem):
    cost: int = 2


class ExpensiveItem(BaseItem):
    cost: int = 500


# ======================================================================================================================
# Type Aliases
# ======================================================================================================================
ListItems = List[BaseItem]
ListCheapItems = List[CheapItem]
ListExpensiveItems = List[CheapItem]
